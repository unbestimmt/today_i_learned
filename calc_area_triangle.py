print("Program to calculate the area of a triangle\n")
base = float(input("Type the value of the base of the triangle: "))
height = float(input("Type the value of the height of the triangle: "))
area = (base*height)/2
print("\nThe area of the triangle of base {:.2f} and height {:.2f} is {:.2f}.\n".format(base, height, area))
print("--- End of program. ---")
