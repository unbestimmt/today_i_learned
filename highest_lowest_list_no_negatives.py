print("Type a list of numbers and the checker will automatically calculate which ones are the highest and the lowest ones, ignoring negative numbers\n")
n = int(input("Type the quantity of numbers you want to add to the list: "))
print(" ")
i = 0
a = []
while i<n:
    number = float(input("Type a real number: "))
    if number < 0:
        i += 1
        continue
    else:
        a.append(number)
    i += 1
print("\nThe lowest number is", min(a))
print("The highest number is", max(a))
